const IMAGE_PIXELS: u32 = 16;
const TIFF_COMPRESSION_JPEG: u64 = 7;
const TIFF_PHOTOMETRIC_YCBCR: u64 = 6;
const TIFF_TAG_IMAGEWIDTH: u16 = 256;
const TIFF_TAG_IMAGELENGTH: u16 = 257;
const TIFF_TAG_COMPRESSION: u16 = 259;
const TIFF_TAG_PHOTOMETRIC: u16 = 262;
const TIFF_TAG_TILEOFFSETS: u16 = 324;
const TIFF_TAG_TILEBYTECOUNTS: u16 = 325;
const TIFF_TAG_JPEGTABLES: u16 = 347;
const DICOM_WSI_SOP_CLASS_UID: &str = "1.2.840.10008.5.1.4.1.1.7.4";

pub fn detect(path: &Path) -> bool {
    path.as_os_str().is_empty() && synthetic_debug_enabled()
}

pub(crate) fn open(path: &Path) -> Result<Box<dyn SlideBackend>> {
    if !path.as_os_str().is_empty() {
        return Err(OpenSlideError::UnsupportedFormat(
            "Unrecognized synthetic filename".into(),
        ));
    }
    if !synthetic_debug_enabled() {
        return Err(OpenSlideError::UnsupportedFormat(
            "synthetic debug flag is not set".into(),
        ));
    }
    Ok(Box::new(SyntheticSlide::new()?))
}

fn synthetic_debug_enabled() -> bool {
    debug::_openslide_debug(OPENSLIDE_DEBUG_SYNTHETIC)
}

struct SyntheticSlide {
    properties: HashMap<String, String>,
    images: Vec<SyntheticImage>,
}

#[derive(Clone)]
struct SyntheticImage {
    rgba: RgbaImage,
}

#[derive(Clone, Copy)]
struct SyntheticItem {
    name: &'static str,
    description: &'static str,
    is_valid: bool,
    is_image: bool,
    decoder: SyntheticDecoder,
    uncompressed_size: usize,
    compressed_data: &'static [u8],
}

#[derive(Clone, Copy)]
enum SyntheticDecoder {
    Bmp,
    Dicom,
    Jpeg2000,
    Jpeg,
    Png,
    Tiff,
    Xml,
    Zstd,
}

impl SyntheticSlide {
    fn new() -> Result<Self> {
        let mut properties = HashMap::new();
        properties.insert(properties::PROPERTY_VENDOR.into(), "synthetic".into());
        properties.insert(properties::PROPERTY_LEVEL_COUNT.into(), "1".into());
        let mut images = Vec::new();
        let mut quickhash1 = OpenslideHash::openslide_hash_quickhash1_create();

        for item in SYNTHETIC_ITEMS {
            match decode_item(item) {
                Ok(Some(rgba)) if item.is_valid && item.is_image => {
                    properties.insert(
                        format!("synthetic.item.{}", item.name),
                        item.description.into(),
                    );
                    properties.insert(
                        format!("synthetic.image[{}]", images.len()),
                        item.name.into(),
                    );
                    images.push(SyntheticImage { rgba });
                }
                Ok(None) if item.is_valid && !item.is_image => {
                    properties.insert(
                        format!("synthetic.item.{}", item.name),
                        format!("{} (not rendered)", item.description),
                    );
                }
                Ok(_) if !item.is_valid => {
                    return Err(OpenSlideError::Decode(format!(
                        "Decoding {}: invalid item decoded successfully",
                        item.name
                    )));
                }
                Err(err) if item.is_valid => {
                    return Err(OpenSlideError::Decode(format!(
                        "Decoding {}: {err}",
                        item.name
                    )));
                }
                Err(err) => {
                    if invalid_item_decoded_successfully(&err) {
                        return Err(err);
                    }
                    properties.insert(
                        format!("synthetic.item.{}", item.name),
                        format!("{} (not rendered)", item.description),
                    );
                }
                Ok(_) => unreachable!("invalid synthetic item handled above"),
            }
            quickhash1.openslide_hash_string(Some(item.name));
            quickhash1.openslide_hash_data(item.compressed_data);
        }
        if let Some(value) = quickhash1.openslide_hash_get_string() {
            properties.insert(properties::PROPERTY_QUICKHASH1.into(), value);
        }

        let width = images.len() as u32 * IMAGE_PIXELS;
        properties.insert(properties::level_width(0), width.to_string());
        properties.insert(properties::level_height(0), IMAGE_PIXELS.to_string());
        properties.insert(properties::level_downsample(0), "1".into());
        Ok(Self { properties, images })
    }

    fn rgba_region(&self, x: i64, y: i64, w: u32, h: u32) -> Result<RgbaImage> {
        let mut out = RgbaImage::new(w, h);
        for out_y in 0..h {
            for out_x in 0..w {
                let src_x = x + out_x as i64;
                let src_y = y + out_y as i64;
                let color = self.synthetic_pixel(src_x, src_y);
                let dst = (out_y as usize * w as usize + out_x as usize) * 4;
                out.data[dst..dst + 4].copy_from_slice(&color);
            }
        }
        Ok(out)
    }

    fn synthetic_pixel(&self, x: i64, y: i64) -> [u8; 4] {
        if y < 0 || y >= IMAGE_PIXELS as i64 || x < 0 {
            return [0, 0, 0, 0];
        }
        let image_index = x / IMAGE_PIXELS as i64;
        let local_x = x % IMAGE_PIXELS as i64;
        let Some(image) = usize::try_from(image_index)
            .ok()
            .and_then(|index| self.images.get(index))
        else {
            return [0, 0, 0, 0];
        };
        image.rgba.pixel(local_x as u32, y as u32)
    }
}

impl SlideBackend for SyntheticSlide {
    fn vendor(&self) -> &'static str {
        "synthetic"
    }

    fn channel_count(&self) -> u32 {
        3
    }

    fn channel_name(&self, _channel: u32) -> Option<&str> {
        None
    }

    fn level_count(&self) -> u32 {
        1
    }

    fn level_dimensions(&self, level: u32) -> Option<(u64, u64)> {
        (level == 0).then_some((
            self.images.len() as u64 * IMAGE_PIXELS as u64,
            IMAGE_PIXELS as u64,
        ))
    }

    fn level_downsample(&self, level: u32) -> Option<f64> {
        (level == 0).then_some(1.0)
    }

    fn level_tile_dimensions(&self, level: u32) -> Option<(u64, u64)> {
        (level == 0).then_some((u64::from(IMAGE_PIXELS), u64::from(IMAGE_PIXELS)))
    }

    fn compressed_level_info(&self, level: u32) -> Result<CompressedExtractionSupport> {
        if level != 0 {
            return Err(OpenSlideError::InvalidArgument(format!(
                "Invalid level {level}"
            )));
        }
        Ok(CompressedExtractionSupport::NotSupported {
            reason: "synthetic slides do not expose source lossy compressed blocks".into(),
        })
    }

    fn read_compressed_tile(
        &self,
        level: u32,
        _col: u64,
        _row: u64,
        _preferred_modes: &[CompressedTileMode],
    ) -> Result<CompressedTile> {
        if level != 0 {
            return Err(OpenSlideError::InvalidArgument(format!(
                "Invalid level {level}"
            )));
        }
        Err(OpenSlideError::UnsupportedFormat(
            "synthetic slides do not expose source lossy compressed blocks".into(),
        ))
    }

    fn read_region(
        &self,
        channel: u32,
        x: i64,
        y: i64,
        level: u32,
        w: u32,
        h: u32,
    ) -> Result<GrayImage> {
        if level != 0 {
            return Err(OpenSlideError::InvalidArgument(format!(
                "Invalid synthetic level: {level}"
            )));
        }
        if channel >= self.channel_count() {
            return Err(OpenSlideError::InvalidArgument(format!(
                "Invalid synthetic channel: {channel}"
            )));
        }
        let rgba = self.rgba_region(x, y, w, h)?;
        let mut gray = GrayImage::new(w, h);
        for pixel in 0..(w as usize * h as usize) {
            gray.data[pixel] = rgba.data[pixel * 4 + channel as usize];
        }
        Ok(gray)
    }

    fn read_region_rgba(
        &self,
        channels: [Option<u32>; 4],
        x: i64,
        y: i64,
        level: u32,
        w: u32,
        h: u32,
    ) -> Result<RgbaImage> {
        if level != 0 {
            return Err(OpenSlideError::InvalidArgument(format!(
                "Invalid synthetic level: {level}"
            )));
        }
        if channels == [Some(0), Some(1), Some(2), None] {
            return self.rgba_region(x, y, w, h);
        }
        let size = w as usize * h as usize;
        let mut rgba = vec![0u8; size * 4];
        if channels[3].is_none() {
            for pixel in rgba.chunks_exact_mut(4) {
                pixel[3] = 255;
            }
        }
        for (out_idx, channel) in channels.into_iter().enumerate() {
            let Some(channel) = channel else {
                continue;
            };
            let gray = self.read_region(channel, x, y, level, w, h)?;
            for pixel in 0..size {
                rgba[pixel * 4 + out_idx] = gray.data[pixel];
            }
        }
        RgbaImage::from_rgba(w, h, rgba)
    }

    fn properties(&self) -> &HashMap<String, String> {
        &self.properties
    }

    fn associated_image_names(&self) -> Vec<&str> {
        Vec::new()
    }

    fn read_associated_image(&self, name: &str) -> Result<RgbaImage> {
        Err(OpenSlideError::InvalidArgument(format!(
            "Unknown synthetic associated image: {name}"
        )))
    }

    fn debug_grid_tile_count(&self, _channel: u32, level: u32) -> usize {
        if level == 0 {
            self.images.len()
        } else {
            0
        }
    }
}

fn decode_item(item: &SyntheticItem) -> Result<Option<RgbaImage>> {
    let data = inflate_item(item)?;
    let image = match match item.decoder {
        SyntheticDecoder::Bmp => Some(decode::bmp::decode_bmp_rgba(&data)?),
        SyntheticDecoder::Dicom => Some(decode_dicom_image(&data)?),
        SyntheticDecoder::Jpeg2000 => Some(decode_jpeg2000_image(&data)?),
        SyntheticDecoder::Jpeg => Some(decode_jpeg_image(&data)?),
        SyntheticDecoder::Png => Some(decode::png::decode_png_rgba(&data)?),
        SyntheticDecoder::Tiff => Some(decode_tiff_image(&data)?),
        SyntheticDecoder::Xml => {
            validate_synthetic_xml(&data)?;
            None
        }
        SyntheticDecoder::Zstd => Some(decode_zstd_image(&data)?),
    } {
        image if item.is_valid => image,
        _ => {
            return Err(OpenSlideError::Decode(format!(
                "Decoding {}: invalid item decoded successfully",
                item.name
            )));
        }
    };
    let Some(image) = image else {
        return Ok(None);
    };
    if image.width != IMAGE_PIXELS || image.height != IMAGE_PIXELS {
        return Err(OpenSlideError::Decode(format!(
            "Synthetic image dimensions {}x{} != {}x{}",
            image.width, image.height, IMAGE_PIXELS, IMAGE_PIXELS
        )));
    }
    if item.is_image {
        validate_image_samples(item, &image)?;
        Ok(Some(image))
    } else {
        Ok(None)
    }
}

fn invalid_item_decoded_successfully(err: &OpenSlideError) -> bool {
    matches!(
        err,
        OpenSlideError::Decode(message) if message.contains("invalid item decoded successfully")
    )
}

fn inflate_item(item: &SyntheticItem) -> Result<Vec<u8>> {
    crate::util::_openslide_inflate_buffer(item.compressed_data, item.uncompressed_size).map_err(
        |err| {
            OpenSlideError::Decode(format!(
                "Synthetic item {} inflate failed: {err}",
                item.name
            ))
        },
    )
}

fn validate_image_samples(item: &SyntheticItem, image: &RgbaImage) -> Result<()> {
    let red = sample_argb(image, 17 * IMAGE_PIXELS as usize / 4);
    let green = sample_argb(image, 19 * IMAGE_PIXELS as usize / 4);
    let blue = sample_argb(image, 49 * IMAGE_PIXELS as usize / 4);
    const HI: u8 = 0xb0;
    const LO: u8 = 0x50;
    let ok = red[0] == 0xff
        && green[0] == 0xff
        && blue[0] == 0xff
        && red[1] > HI
        && green[2] > HI
        && blue[3] > HI
        && red[2] < LO
        && red[3] < LO
        && green[1] < LO
        && green[3] < LO
        && blue[1] < LO
        && blue[2] < LO;
    if !ok {
        return Err(OpenSlideError::Decode(format!(
            "Invalid image samples for {}: red {:02x?}, green {:02x?}, blue {:02x?}",
            item.name, red, green, blue
        )));
    }
    Ok(())
}

fn sample_argb(image: &RgbaImage, pixel: usize) -> [u8; 4] {
    let base = pixel * 4;
    [
        image.data[base + 3],
        image.data[base],
        image.data[base + 1],
        image.data[base + 2],
    ]
}

fn validate_synthetic_xml(data: &[u8]) -> Result<()> {
    let text = std::str::from_utf8(data).map_err(|err| {
        OpenSlideError::Decode(format!("Synthetic XML is not valid UTF-8: {err}"))
    })?;
    if !text.starts_with("<?xml version=\"1.0\"?>") {
        return Err(OpenSlideError::Decode(
            "Synthetic XML missing declaration".into(),
        ));
    }
    if !text.contains("<top xmlns=\"https://openslide.org/xml/nonsense\">")
        || !text.contains("<inner one=\"1\" two=\"two\"/>")
        || !text.contains("</top>")
    {
        return Err(OpenSlideError::Decode(
            "Synthetic XML document has unexpected structure".into(),
        ));
    }
    Ok(())
}

fn decode_dicom_image(data: &[u8]) -> Result<RgbaImage> {
    let frame = synthetic_dicom_jpeg_frame(data)?;
    decode_jpeg_image(frame)
}

fn synthetic_dicom_jpeg_frame(data: &[u8]) -> Result<&[u8]> {
    if data.len() < 132 || data.get(128..132) != Some(b"DICM") {
        return Err(OpenSlideError::Decode(
            "Synthetic DICOM missing DICM preamble".into(),
        ));
    }

    let mut pos = 132;
    let mut media_storage_sop_class = None;
    let mut transfer_syntax = None;
    let mut rows = None;
    let mut columns = None;
    let mut frame = None;

    while pos + 8 <= data.len() {
        let group = synthetic_read_le_u16(data, pos, "DICOM tag group")?;
        let element = synthetic_read_le_u16(data, pos + 2, "DICOM tag element")?;
        let vr = data
            .get(pos + 4..pos + 6)
            .ok_or_else(|| OpenSlideError::Decode("Truncated synthetic DICOM VR".into()))?;
        let (length, value_start) = if synthetic_dicom_vr_has_u32_length(vr) {
            (
                synthetic_read_le_u32(data, pos + 8, "DICOM value length")?,
                pos + 12,
            )
        } else {
            (
                synthetic_read_le_u16(data, pos + 6, "DICOM value length")? as u32,
                pos + 8,
            )
        };

        if (group, element) == (0x7fe0, 0x0010) {
            if length != u32::MAX {
                return Err(OpenSlideError::Decode(
                    "Synthetic DICOM PixelData is not encapsulated".into(),
                ));
            }
            frame = Some(synthetic_dicom_first_encapsulated_frame(data, value_start)?);
            break;
        }

        if length == u32::MAX {
            return Err(OpenSlideError::Decode(
                "Synthetic DICOM has unexpected undefined-length element".into(),
            ));
        }
        let length = length as usize;
        let value_end = value_start
            .checked_add(length)
            .ok_or_else(|| OpenSlideError::Decode("Synthetic DICOM value overflow".into()))?;
        let value = data
            .get(value_start..value_end)
            .ok_or_else(|| OpenSlideError::Decode("Synthetic DICOM value is truncated".into()))?;

        match (group, element) {
            (0x0002, 0x0002) => media_storage_sop_class = Some(synthetic_dicom_text(value)?),
            (0x0002, 0x0010) => transfer_syntax = Some(synthetic_dicom_text(value)?),
            (0x0028, 0x0010) => rows = Some(synthetic_read_le_u16(value, 0, "DICOM Rows")?),
            (0x0028, 0x0011) => columns = Some(synthetic_read_le_u16(value, 0, "DICOM Columns")?),
            _ => {}
        }

        pos = value_end.checked_add(length & 1).ok_or_else(|| {
            OpenSlideError::Decode("Synthetic DICOM value padding overflow".into())
        })?;
    }

    if media_storage_sop_class.as_deref() != Some(DICOM_WSI_SOP_CLASS_UID) {
        return Err(OpenSlideError::Decode(
            "Synthetic DICOM has unexpected MediaStorageSOPClassUID".into(),
        ));
    }
    if transfer_syntax.as_deref() != Some("1.2.840.10008.1.2.4.50") {
        return Err(OpenSlideError::Decode(
            "Synthetic DICOM has unexpected TransferSyntaxUID".into(),
        ));
    }
    if rows != Some(IMAGE_PIXELS as u16) || columns != Some(IMAGE_PIXELS as u16) {
        return Err(OpenSlideError::Decode(
            "Synthetic DICOM has unexpected image dimensions".into(),
        ));
    }
    frame.ok_or_else(|| OpenSlideError::Decode("Synthetic DICOM missing JPEG frame".into()))
}

fn synthetic_dicom_vr_has_u32_length(vr: &[u8]) -> bool {
    matches!(
        vr,
        b"OB" | b"OD" | b"OF" | b"OL" | b"OW" | b"SQ" | b"UC" | b"UR" | b"UT" | b"UN"
    )
}

fn synthetic_dicom_first_encapsulated_frame(data: &[u8], mut pos: usize) -> Result<&[u8]> {
    synthetic_dicom_expect_item(data, pos, "DICOM Basic Offset Table")?;
    let bot_length =
        synthetic_read_le_u32(data, pos + 4, "DICOM Basic Offset Table length")? as usize;
    pos = pos
        .checked_add(8)
        .and_then(|pos| pos.checked_add(bot_length))
        .ok_or_else(|| OpenSlideError::Decode("Synthetic DICOM item overflow".into()))?;
    if data.get(pos - bot_length..pos).is_none() {
        return Err(OpenSlideError::Decode(
            "Synthetic DICOM Basic Offset Table is truncated".into(),
        ));
    }

    synthetic_dicom_expect_item(data, pos, "DICOM first frame item")?;
    let frame_length = synthetic_read_le_u32(data, pos + 4, "DICOM frame length")? as usize;
    let frame_start = pos + 8;
    let frame_end = frame_start
        .checked_add(frame_length)
        .ok_or_else(|| OpenSlideError::Decode("Synthetic DICOM frame overflow".into()))?;
    data.get(frame_start..frame_end)
        .ok_or_else(|| OpenSlideError::Decode("Synthetic DICOM frame is truncated".into()))
}

fn synthetic_dicom_expect_item(data: &[u8], pos: usize, context: &str) -> Result<()> {
    let group = synthetic_read_le_u16(data, pos, context)?;
    let element = synthetic_read_le_u16(data, pos + 2, context)?;
    if (group, element) != (0xfffe, 0xe000) {
        return Err(OpenSlideError::Decode(format!(
            "Synthetic {context} is not an item"
        )));
    }
    Ok(())
}

fn synthetic_dicom_text(value: &[u8]) -> Result<String> {
    let text = std::str::from_utf8(value)
        .map_err(|err| OpenSlideError::Decode(format!("Synthetic DICOM text is invalid: {err}")))?;
    Ok(text.trim_end_matches('\0').trim_end().to_string())
}

fn synthetic_read_le_u16(data: &[u8], offset: usize, context: &str) -> Result<u16> {
    let bytes = data
        .get(offset..offset + 2)
        .ok_or_else(|| OpenSlideError::Decode(format!("Truncated synthetic {context}")))?;
    Ok(u16::from_le_bytes([bytes[0], bytes[1]]))
}

fn synthetic_read_le_u32(data: &[u8], offset: usize, context: &str) -> Result<u32> {
    let bytes = data
        .get(offset..offset + 4)
        .ok_or_else(|| OpenSlideError::Decode(format!("Truncated synthetic {context}")))?;
    Ok(u32::from_le_bytes([bytes[0], bytes[1], bytes[2], bytes[3]]))
}

fn decode_jpeg_image(data: &[u8]) -> Result<RgbaImage> {
    let (rgb, width, height) = decode::jpeg::decode_jpeg_rgb_libjpeg(data)?;
    let mut rgba = Vec::with_capacity(rgb.len() / 3 * 4);
    for pixel in rgb.chunks_exact(3) {
        rgba.extend_from_slice(pixel);
        rgba.push(0xff);
    }
    RgbaImage::from_rgba(width, height, rgba)
}

fn decode_jpeg2000_image(data: &[u8]) -> Result<RgbaImage> {
    let (rgb, width, height) = decode::default_decoder_api().decode_jpeg2000_rgb(
        data,
        decode::jpeg2000::Jpeg2000DecodeOptions::new(
            IMAGE_PIXELS,
            IMAGE_PIXELS,
            3,
            decode::jpeg2000::Jpeg2000OutputFormat::Rgb,
            "synthetic J2K",
        ),
    )?;
    let mut rgba = Vec::with_capacity(rgb.len() / 3 * 4);
    for pixel in rgb.chunks_exact(3) {
        rgba.extend_from_slice(pixel);
        rgba.push(0xff);
    }
    RgbaImage::from_rgba(width, height, rgba)
}

fn decode_zstd_image(data: &[u8]) -> Result<RgbaImage> {
    use zstd_pure_rs::prelude::{
        ZSTD_decompress, ZSTD_getFrameContentSize, ZSTD_isError, ZSTD_CONTENTSIZE_ERROR,
    };

    let content_size = ZSTD_getFrameContentSize(data);
    if content_size == ZSTD_CONTENTSIZE_ERROR {
        return Err(OpenSlideError::Decode(
            "Synthetic Zstd item has an invalid frame header".into(),
        ));
    }

    let expected = IMAGE_PIXELS as usize * IMAGE_PIXELS as usize * 4;
    let mut argb = vec![0u8; expected];
    let written = ZSTD_decompress(&mut argb, data);
    if ZSTD_isError(written) {
        return Err(OpenSlideError::Decode(format!(
            "Synthetic Zstd decode error code {written}"
        )));
    }
    if written != expected {
        return Err(OpenSlideError::Decode(format!(
            "Synthetic Zstd decoded to {written} bytes, expected {expected}"
        )));
    }

    let mut rgba = Vec::with_capacity(expected);
    for pixel in argb.chunks_exact(4) {
        rgba.push(pixel[1]);
        rgba.push(pixel[2]);
        rgba.push(pixel[3]);
        rgba.push(pixel[0]);
    }
    RgbaImage::from_rgba(IMAGE_PIXELS, IMAGE_PIXELS, rgba)
}

fn decode_tiff_image(data: &[u8]) -> Result<RgbaImage> {
    if let Some(info) = parse_synthetic_tiff_info(data)? {
        if info.compression == TIFF_COMPRESSION_JPEG {
            return decode_synthetic_tiff_jpeg(data, &info);
        }
    }

    let mut decoder = ::tiff::decoder::Decoder::new(Cursor::new(data))
        .map_err(|err| OpenSlideError::Decode(format!("Synthetic TIFF setup failed: {err}")))?;
    let (width, height) = decoder.dimensions().map_err(|err| {
        OpenSlideError::Decode(format!("Synthetic TIFF dimensions failed: {err}"))
    })?;
    let color_type = decoder.colortype().map_err(|err| {
        OpenSlideError::Decode(format!("Synthetic TIFF color type failed: {err}"))
    })?;
    let image = decoder.read_chunk(0).map_err(|err| {
        OpenSlideError::Decode(format!("Synthetic TIFF chunk decode failed: {err}"))
    })?;
    synthetic_tiff_chunk_to_rgba(image, color_type, width, height)
}

struct SyntheticTiffInfo {
    width: u32,
    height: u32,
    compression: u64,
    photometric: u64,
    tile_offset: u64,
    tile_byte_count: u64,
    jpeg_tables: Option<Vec<u8>>,
}

#[derive(Clone, Copy)]
enum SyntheticTiffEndian {
    Little,
    Big,
}

impl SyntheticTiffEndian {
    fn read_u16(self, bytes: &[u8]) -> Result<u16> {
        let slice = bytes
            .get(..2)
            .ok_or_else(|| OpenSlideError::Decode("Truncated synthetic TIFF u16".into()))?;
        Ok(match self {
            Self::Little => u16::from_le_bytes([slice[0], slice[1]]),
            Self::Big => u16::from_be_bytes([slice[0], slice[1]]),
        })
    }

    fn read_u32(self, bytes: &[u8]) -> Result<u32> {
        let slice = bytes
            .get(..4)
            .ok_or_else(|| OpenSlideError::Decode("Truncated synthetic TIFF u32".into()))?;
        Ok(match self {
            Self::Little => u32::from_le_bytes([slice[0], slice[1], slice[2], slice[3]]),
            Self::Big => u32::from_be_bytes([slice[0], slice[1], slice[2], slice[3]]),
        })
    }

    fn read_u64(self, bytes: &[u8]) -> Result<u64> {
        let slice = bytes
            .get(..8)
            .ok_or_else(|| OpenSlideError::Decode("Truncated synthetic TIFF u64".into()))?;
        Ok(match self {
            Self::Little => u64::from_le_bytes([
                slice[0], slice[1], slice[2], slice[3], slice[4], slice[5], slice[6], slice[7],
            ]),
            Self::Big => u64::from_be_bytes([
                slice[0], slice[1], slice[2], slice[3], slice[4], slice[5], slice[6], slice[7],
            ]),
        })
    }
}

struct SyntheticTiffEntry<'a> {
    tag: u16,
    value_type: u16,
    count: u64,
    value_slot: &'a [u8],
}

fn parse_synthetic_tiff_info(data: &[u8]) -> Result<Option<SyntheticTiffInfo>> {
    if data.len() < 8 {
        return Ok(None);
    }
    let endian = match data.get(..2) {
        Some(b"II") => SyntheticTiffEndian::Little,
        Some(b"MM") => SyntheticTiffEndian::Big,
        _ => return Ok(None),
    };
    let magic = endian.read_u16(&data[2..4])?;
    let (ifd_offset, entry_count, entry_base, entry_len, value_slot_len) = match magic {
        42 => {
            let offset = endian.read_u32(&data[4..8])? as usize;
            let count = endian.read_u16(data.get(offset..).ok_or_else(|| {
                OpenSlideError::Decode("Synthetic classic TIFF IFD offset is out of range".into())
            })?)? as usize;
            (offset, count, offset + 2, 12, 4)
        }
        43 => {
            if data.len() < 16 {
                return Err(OpenSlideError::Decode(
                    "Synthetic BigTIFF header is truncated".into(),
                ));
            }
            let bytesize = endian.read_u16(&data[4..6])?;
            let reserved = endian.read_u16(&data[6..8])?;
            if bytesize != 8 || reserved != 0 {
                return Err(OpenSlideError::Decode(
                    "Synthetic BigTIFF header has unsupported offset size".into(),
                ));
            }
            let offset = usize::try_from(endian.read_u64(&data[8..16])?).map_err(|_| {
                OpenSlideError::Decode("Synthetic BigTIFF IFD offset too large".into())
            })?;
            let count =
                usize::try_from(endian.read_u64(data.get(offset..).ok_or_else(|| {
                    OpenSlideError::Decode("Synthetic BigTIFF IFD offset is out of range".into())
                })?)?)
                .map_err(|_| {
                    OpenSlideError::Decode("Synthetic BigTIFF IFD count too large".into())
                })?;
            (offset, count, offset + 8, 20, 8)
        }
        _ => return Ok(None),
    };
    let entries_len = entry_count
        .checked_mul(entry_len)
        .ok_or_else(|| OpenSlideError::Decode("Synthetic TIFF IFD size overflow".into()))?;
    if data.len() < entry_base.saturating_add(entries_len) || ifd_offset >= data.len() {
        return Err(OpenSlideError::Decode(
            "Synthetic TIFF IFD entries are truncated".into(),
        ));
    }

    let mut width = None;
    let mut height = None;
    let mut compression = None;
    let mut photometric = None;
    let mut tile_offset = None;
    let mut tile_byte_count = None;
    let mut jpeg_tables = None;

    for idx in 0..entry_count {
        let start = entry_base + idx * entry_len;
        let entry = synthetic_tiff_entry(endian, &data[start..start + entry_len], value_slot_len)?;
        match entry.tag {
            TIFF_TAG_IMAGEWIDTH => width = Some(synthetic_tiff_entry_u64(data, endian, &entry)?),
            TIFF_TAG_IMAGELENGTH => height = Some(synthetic_tiff_entry_u64(data, endian, &entry)?),
            TIFF_TAG_COMPRESSION => {
                compression = Some(synthetic_tiff_entry_u64(data, endian, &entry)?)
            }
            TIFF_TAG_PHOTOMETRIC => {
                photometric = Some(synthetic_tiff_entry_u64(data, endian, &entry)?)
            }
            TIFF_TAG_TILEOFFSETS => {
                tile_offset = Some(synthetic_tiff_entry_u64(data, endian, &entry)?)
            }
            TIFF_TAG_TILEBYTECOUNTS => {
                tile_byte_count = Some(synthetic_tiff_entry_u64(data, endian, &entry)?)
            }
            TIFF_TAG_JPEGTABLES => {
                jpeg_tables = Some(synthetic_tiff_entry_bytes(data, endian, &entry)?.to_vec())
            }
            _ => {}
        }
    }

    let Some(compression) = compression else {
        return Ok(None);
    };
    let Some(width) = width else {
        return Ok(None);
    };
    let Some(height) = height else {
        return Ok(None);
    };
    let Some(photometric) = photometric else {
        return Ok(None);
    };
    let Some(tile_offset) = tile_offset else {
        return Ok(None);
    };
    let Some(tile_byte_count) = tile_byte_count else {
        return Ok(None);
    };
    Ok(Some(SyntheticTiffInfo {
        width: u32::try_from(width)
            .map_err(|_| OpenSlideError::Decode("Synthetic TIFF width too large".into()))?,
        height: u32::try_from(height)
            .map_err(|_| OpenSlideError::Decode("Synthetic TIFF height too large".into()))?,
        compression,
        photometric,
        tile_offset,
        tile_byte_count,
        jpeg_tables,
    }))
}

fn synthetic_tiff_entry<'a>(
    endian: SyntheticTiffEndian,
    bytes: &'a [u8],
    value_slot_len: usize,
) -> Result<SyntheticTiffEntry<'a>> {
    let tag = endian.read_u16(&bytes[0..2])?;
    let value_type = endian.read_u16(&bytes[2..4])?;
    let (count, value_slot) = if value_slot_len == 4 {
        (endian.read_u32(&bytes[4..8])? as u64, &bytes[8..12])
    } else {
        (endian.read_u64(&bytes[4..12])?, &bytes[12..20])
    };
    Ok(SyntheticTiffEntry {
        tag,
        value_type,
        count,
        value_slot,
    })
}

fn synthetic_tiff_entry_u64(
    data: &[u8],
    endian: SyntheticTiffEndian,
    entry: &SyntheticTiffEntry<'_>,
) -> Result<u64> {
    let bytes = synthetic_tiff_entry_bytes(data, endian, entry)?;
    match entry.value_type {
        1 | 7 => bytes
            .first()
            .copied()
            .map(u64::from)
            .ok_or_else(|| OpenSlideError::Decode("Synthetic TIFF byte value is empty".into())),
        3 => endian.read_u16(bytes).map(u64::from),
        4 => endian.read_u32(bytes).map(u64::from),
        16 => endian.read_u64(bytes),
        other => Err(OpenSlideError::Decode(format!(
            "Unsupported synthetic TIFF integer type {other}"
        ))),
    }
}

fn synthetic_tiff_entry_bytes<'a>(
    data: &'a [u8],
    endian: SyntheticTiffEndian,
    entry: &SyntheticTiffEntry<'a>,
) -> Result<&'a [u8]> {
    let type_size = match entry.value_type {
        1 | 2 | 6 | 7 => 1,
        3 | 8 => 2,
        4 | 9 => 4,
        5 | 10 | 16 => 8,
        other => {
            return Err(OpenSlideError::Decode(format!(
                "Unsupported synthetic TIFF value type {other}"
            )))
        }
    };
    let len = usize::try_from(entry.count)
        .ok()
        .and_then(|count| count.checked_mul(type_size))
        .ok_or_else(|| OpenSlideError::Decode("Synthetic TIFF value size overflow".into()))?;
    if len <= entry.value_slot.len() {
        return Ok(&entry.value_slot[..len]);
    }
    let offset = if entry.value_slot.len() == 4 {
        usize::try_from(endian.read_u32(entry.value_slot)?)
            .map_err(|_| OpenSlideError::Decode("Synthetic TIFF value offset too large".into()))?
    } else {
        usize::try_from(endian.read_u64(entry.value_slot)?)
            .map_err(|_| OpenSlideError::Decode("Synthetic TIFF value offset too large".into()))?
    };
    let end = offset
        .checked_add(len)
        .ok_or_else(|| OpenSlideError::Decode("Synthetic TIFF value range overflow".into()))?;
    data.get(offset..end).ok_or_else(|| {
        OpenSlideError::Decode(format!(
            "Synthetic TIFF value for tag {} is out of range",
            entry.tag
        ))
    })
}

fn decode_synthetic_tiff_jpeg(data: &[u8], info: &SyntheticTiffInfo) -> Result<RgbaImage> {
    let offset = usize::try_from(info.tile_offset)
        .map_err(|_| OpenSlideError::Decode("Synthetic TIFF tile offset too large".into()))?;
    let byte_count = usize::try_from(info.tile_byte_count)
        .map_err(|_| OpenSlideError::Decode("Synthetic TIFF tile byte count too large".into()))?;
    let end = offset
        .checked_add(byte_count)
        .ok_or_else(|| OpenSlideError::Decode("Synthetic TIFF tile range overflow".into()))?;
    let raw = data.get(offset..end).ok_or_else(|| {
        OpenSlideError::Decode("Synthetic TIFF JPEG tile range is out of bounds".into())
    })?;
    let color_space = if info.photometric == TIFF_PHOTOMETRIC_YCBCR {
        2
    } else {
        1
    };
    let (rgb, width, height) = decode::decode_tiff_bgra_rgb_region(
        decode::ImageFormat::Jpeg,
        raw,
        info.jpeg_tables.as_deref(),
        0,
        0,
        info.width,
        info.height,
        color_space,
    )?;
    let mut rgba = Vec::with_capacity(rgb.len() / 3 * 4);
    for pixel in rgb.chunks_exact(3) {
        rgba.extend_from_slice(pixel);
        rgba.push(0xff);
    }
    RgbaImage::from_rgba(width, height, rgba)
}

fn synthetic_tiff_chunk_to_rgba(
    image: ::tiff::decoder::DecodingResult,
    color_type: ::tiff::ColorType,
    width: u32,
    height: u32,
) -> Result<RgbaImage> {
    let stride = match color_type {
        ::tiff::ColorType::Gray(8) | ::tiff::ColorType::Gray(16) => 1,
        ::tiff::ColorType::GrayA(8) | ::tiff::ColorType::GrayA(16) => 2,
        ::tiff::ColorType::RGB(8) | ::tiff::ColorType::RGB(16) => 3,
        ::tiff::ColorType::RGBA(8) | ::tiff::ColorType::RGBA(16) => 4,
        other => {
            return Err(OpenSlideError::Decode(format!(
                "Unsupported synthetic TIFF color type: {other:?}"
            )))
        }
    };
    let pixel_count = width as usize * height as usize;
    let expected = pixel_count.saturating_mul(stride);
    match &image {
        ::tiff::decoder::DecodingResult::U8(data) if data.len() < expected => {
            return Err(OpenSlideError::Decode(
                "Decoded synthetic TIFF chunk is truncated".into(),
            ))
        }
        ::tiff::decoder::DecodingResult::U16(data) if data.len() < expected => {
            return Err(OpenSlideError::Decode(
                "Decoded synthetic TIFF chunk is truncated".into(),
            ))
        }
        ::tiff::decoder::DecodingResult::U8(_) | ::tiff::decoder::DecodingResult::U16(_) => {}
        other => {
            return Err(OpenSlideError::Decode(format!(
                "Unsupported synthetic TIFF sample type: {other:?}"
            )))
        }
    }

    let mut rgba = Vec::with_capacity(pixel_count * 4);
    for pixel in 0..pixel_count {
        let src = pixel * stride;
        match color_type {
            ::tiff::ColorType::Gray(8) | ::tiff::ColorType::Gray(16) => {
                let value = synthetic_tiff_sample_u8(&image, src);
                rgba.extend_from_slice(&[value, value, value, 0xff]);
            }
            ::tiff::ColorType::GrayA(8) | ::tiff::ColorType::GrayA(16) => {
                let value = synthetic_tiff_sample_u8(&image, src);
                let alpha = synthetic_tiff_sample_u8(&image, src + 1);
                rgba.extend_from_slice(&[value, value, value, alpha]);
            }
            ::tiff::ColorType::RGB(8) | ::tiff::ColorType::RGB(16) => {
                rgba.push(synthetic_tiff_sample_u8(&image, src));
                rgba.push(synthetic_tiff_sample_u8(&image, src + 1));
                rgba.push(synthetic_tiff_sample_u8(&image, src + 2));
                rgba.push(0xff);
            }
            ::tiff::ColorType::RGBA(8) | ::tiff::ColorType::RGBA(16) => {
                rgba.push(synthetic_tiff_sample_u8(&image, src));
                rgba.push(synthetic_tiff_sample_u8(&image, src + 1));
                rgba.push(synthetic_tiff_sample_u8(&image, src + 2));
                rgba.push(synthetic_tiff_sample_u8(&image, src + 3));
            }
            _ => unreachable!("checked above"),
        }
    }
    RgbaImage::from_rgba(width, height, rgba)
}

fn synthetic_tiff_sample_u8(image: &::tiff::decoder::DecodingResult, index: usize) -> u8 {
    match image {
        ::tiff::decoder::DecodingResult::U8(data) => data[index],
        ::tiff::decoder::DecodingResult::U16(data) => (data[index] >> 8) as u8,
        _ => unreachable!(),
    }
}

